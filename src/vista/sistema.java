    private void Total(){
        totalpagar=0;
        int numFila = jtCompra.getRowCount();
        for (int i = 0; i < numFila; i++) {
            float cal = Float.parseFloat(""+jtCompra.getModel().getValueAt(i, 4));
            totalpagar = totalpagar+cal;
        }
        jtfTotalC.setText(""+String.format("%.2f",totalpagar));
    }
